Ssd1306::Ssd1306(i2c_master_bus_handle_t bus) : bus_(bus) {}

Ssd1306::~Ssd1306() {}

void Ssd1306::Init() {
    ESP_LOGI(TAG, "Init SSD1306 I2C");
    // Add OLED device to bus
    i2c_device_config_t dev_cfg = {};
    dev_cfg.dev_addr_length = I2C_ADDR_BIT_LEN_7;
    dev_cfg.device_address = SSD1306_I2C_ADDR;
    dev_cfg.scl_speed_hz = 100000;
    ESP_ERROR_CHECK(i2c_master_bus_add_device(bus_, &dev_cfg, &dev_));
    ESP_LOGI(TAG, "OLED device added on bus");

    // Send init sequence
    uint8_t cmd[2] = {SSD1306_CMD_PREFIX, 0};
    for (size_t i = 0; i < sizeof(INIT_SEQUENCE); i++) {
        cmd[1] = INIT_SEQUENCE[i];
        esp_err_t err = i2c_master_transmit(dev_, cmd, 2, -1);
        if (err != ESP_OK) {
            ESP_LOGE(TAG, "SSD1306 init cmd 0x%02X failed: %s", INIT_SEQUENCE[i], esp_err_to_name(err));
        }
    }
    ESP_LOGI(TAG, "SSD1306 init done");
}

void Ssd1306::SetPixel(int x, int y, bool on) {
    if (x < 0 || x >= WIDTH || y < 0 || y >= HEIGHT) return;
    uint8_t page = y / 8;
    uint8_t bit = y % 8;
    size_t idx = page * WIDTH + x;
    if (on) {
        buffer_[idx] |= (1 << bit);
    } else {
        buffer_[idx] &= ~(1 << bit);
    }
}

void Ssd1306::HLine(int x, int y, int len, bool on) {
    for (int i = 0; i < len; i++) SetPixel(x + i, y, on);
}

void Ssd1306::VLine(int x, int y, int len, bool on) {
    for (int i = 0; i < len; i++) SetPixel(x, y + i, on);
}

void Ssd1306::DrawRect(int x, int y, int w, int h, bool on) {
    if (w <= 0 || h <= 0) return;
    HLine(x, y, w, on);
    HLine(x, y + h - 1, w, on);
    VLine(x, y, h, on);
    VLine(x + w - 1, y, h, on);
}

void Ssd1306::DrawText(int x, int y, const char* text, bool on) {
    int px = x;
    int len = strlen(text);
    for (int c = 0; c < len; c++) {
        unsigned char ch = (unsigned char)text[c];
        if (ch < 32 || ch > 126) ch = 127;
        const uint8_t* glyph = FONT[ch - 32];
        for (int col = 0; col < 5; col++) {
            uint8_t bits = glyph[col];
            for (int row = 0; row < 7; row++) {
                if (bits & (1 << row)) SetPixel(px + col, y + row, on);
            }
        }
        // Space between chars
        px += 6;
        if (px >= WIDTH) break;
    }
}

void Ssd1306::Clear() {
    memset(buffer_, 0, sizeof(buffer_));
}

void Ssd1306::Update() {
    // Set column address 0..127
    uint8_t col_cmd[] = {SSD1306_CMD_PREFIX, CMD_COLUMN_ADDR, 0x00, 0x7F};
    i2c_master_transmit(dev_, col_cmd, 4, -1);
    // Set page address 0..7
    uint8_t page_cmd[] = {SSD1306_CMD_PREFIX, CMD_PAGE_ADDR, 0x00, 0x07};
    i2c_master_transmit(dev_, page_cmd, 4, -1);

    // Send all data bytes
    uint8_t buf[WIDTH + 1];
    buf[0] = SSD1306_DATA_PREFIX;
    for (int page = 0; page < PAGE_COUNT; page++) {
        memcpy(&buf[1], &buffer_[page * WIDTH], WIDTH);
        i2c_master_transmit(dev_, buf, WIDTH + 1, -1);
    }
}

void Ssd1306::Fill(bool on) {
    memset(buffer_, on ? 0xFF : 0x00, sizeof(buffer_));
}

void Ssd1306::Display() {
    Update();
}
