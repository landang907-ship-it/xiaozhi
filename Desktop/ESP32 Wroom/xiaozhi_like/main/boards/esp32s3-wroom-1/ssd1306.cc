Ssd1306::Ssd1306(i2c_master_bus_handle_t bus, uint8_t addr, int width, int height)
    : bus_(bus), addr_(addr), width_(width), height_(height) {
    fb_.assign((width_ * height_) / 8, 0);
}

Ssd1306::~Ssd1306() {
    if (dev_) {
        i2c_master_bus_rm_device(dev_);
        dev_ = nullptr;
    }
}

esp_err_t Ssd1306::WriteCommands(const uint8_t* cmds, size_t n) {
    if (dev_ == nullptr) return ESP_ERR_INVALID_STATE;
    // SSD1306 I2C: control byte 0x00 = command stream follows
    std::vector<uint8_t> buf(n + 1);
    buf[0] = 0x00;
    std::memcpy(&buf[1], cmds, n);
    return i2c_master_transmit(dev_, buf.data(), buf.size(), 50 / portTICK_PERIOD_MS);
}

esp_err_t Ssd1306::WriteFramebuffer() {
    if (dev_ == nullptr) return ESP_ERR_INVALID_STATE;
    // Set full window first: column 0..127, page 0..7
    uint8_t setup[] = {
        0x21, 0x00, (uint8_t)(width_ - 1),   // column address
        0x22, 0x00, (uint8_t)((height_ / 8) - 1)  // page address
    };
    esp_err_t ret = WriteCommands(setup, sizeof(setup));
    if (ret != ESP_OK) return ret;
    // Send framebuffer: control=0x40 + 1024 bytes data
    std::vector<uint8_t> buf(fb_.size() + 1);
    buf[0] = 0x40;
    std::memcpy(&buf[1], fb_.data(), fb_.size());
    return i2c_master_transmit(dev_, buf.data(), buf.size(), 200 / portTICK_PERIOD_MS);
}

void Ssd1306::Init() {
    if (bus_ == nullptr) {
        ESP_LOGE(TAG, "Init: i2c bus null");
        return;
    }
    // Add this OLED as a device on the bus (new IDF I2C master API requires dev handle).
    if (dev_ == nullptr) {
        i2c_device_config_t dev_cfg = {};
        dev_cfg.device_address = addr_;
        dev_cfg.scl_speed_hz = 400000;
        esp_err_t add_ret = i2c_master_bus_add_device(bus_, &dev_cfg, &dev_);
        if (add_ret != ESP_OK) {
            ESP_LOGE(TAG, "i2c_master_bus_add_device failed: %s", esp_err_to_name(add_ret));
            return;
        }
    }
    // Standard SSD1306 128x64 init sequence (Adafruit_SSD1306 / U8g2 compatible).
    static const uint8_t init_cmds[] = {
        0xAE,           // display off
        0xD5, 0x80,     // set display clock divide ratio (default)
        0xA8, 0x3F,     // set multiplex (64-1 = 63)
        0xD3, 0x00,     // set display offset = 0
        0x40,           // set start line = 0
        0x8D, 0x14,     // enable charge pump
        0x20, 0x00,     // memory addressing mode = horizontal
        0xA1,           // segment remap (mirror X)
        0xC8,           // COM output scan direction (mirror Y)
        0xDA, 0x12,     // COM pins hardware config (alt + sequential)
        0x81, 0xCF,     // set contrast
        0xD9, 0xF1,     // set pre-charge period
        0xDB, 0x40,     // set VCOMH deselect level
        0xA4,           // display from RAM (not all-on)
        0xA6,           // normal display (not inverted)
        0x2E,           // deactivate scroll
        0xAF            // display ON
    };
    esp_err_t ret = WriteCommands(init_cmds, sizeof(init_cmds));
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "SSD1306 init failed: %s", esp_err_to_name(ret));
        return;
    }
    ESP_LOGI(TAG, "SSD1306 %dx%d @ 0x%02X initialized", width_, height_, addr_);
    Clear();
    Display();
}

void Ssd1306::Clear() {
    std::memset(fb_.data(), 0, fb_.size());
}

void Ssd1306::SetPixel(int x, int y, uint8_t color) {
    if (x < 0 || x >= width_ || y < 0 || y >= height_) return;
    int page = y / 8;
    int bit = y % 8;
    int idx = page * width_ + x;
    if (color) {
        fb_[idx] |= (1 << bit);
    } else {
        fb_[idx] &= ~(1 << bit);
    }
}

void Ssd1306::DrawChar(int x, int y, char ch, uint8_t color) {
    if (ch < 0x20 || ch > 0x7E) ch = '?';
    const uint8_t* glyph = font5x7[ch - 0x20];
    for (int col = 0; col < 5; col++) {
        uint8_t line = glyph[col];
        for (int row = 0; row < 7; row++) {
            if (line & (1 << row)) {
                SetPixel(x + col, y + row, color);
            }
        }
    }
    // 1-pixel column gap between chars
    for (int row = 0; row < 7; row++) {
        SetPixel(x + 5, y + row, 0);
    }
}

void Ssd1306::DrawText(int x, int y, const char* str, uint8_t color) {
    if (str == nullptr) return;
    int cx = x;
    while (*str) {
        DrawChar(cx, y, *str, color);
        cx += 6;  // 5 + 1 spacing
        str++;
    }
}

void Ssd1306::DrawRect(int x, int y, int w, int h, uint8_t color) {
    for (int i = 0; i < w; i++) {
        SetPixel(x + i, y, color);
        SetPixel(x + i, y + h - 1, color);
    }
    for (int i = 0; i < h; i++) {
        SetPixel(x, y + i, color);
        SetPixel(x + w - 1, y + i, color);
    }
}

void Ssd1306::Display() {
    esp_err_t ret = WriteFramebuffer();
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Display flush failed: %s", esp_err_to_name(ret));
    }
}
